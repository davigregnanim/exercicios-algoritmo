# Solicite um número de 1 a 7 e mostre o dia da semana.

num = int(input("Digite um número (1-7): "))

if num == 1:
    print("Segunda-feira")
elif num == 2:
    print("Terça-feira")
elif num == 3:
    print("Quarta-feira")
elif num == 4:
    print("Quinta-feira")
elif num == 5:
    print("Sexta-feira")
elif num == 6:
    print("Sábado")
else:
    print("Domingo")